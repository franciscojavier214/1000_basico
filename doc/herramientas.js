
/*
    Instalación de Mongodb
Primero nos dirigimos a la pagina https://www.mongodb.com/download-center/community , hacemos 
click en ""Download", en lado derecho de la web y esperamos su descarga, cuando este lista abra 
el documento .msi y ejecutelo, en la siguiente ventada cliquee en "Next" , en la posterior 
presione "Cmplete" y de nuevo "Next", nos aseguramos de que en la nueva ventana este selecionado
el primer checkbox y la casilla "Run service as Network Service user",en la penultima venta
desmarque el checkbox  "Install MongoDB Compass" y cliquee nuevamente "Next", en la ultima 
ventana ,presione Install.
Vaya al panel de control, Sistema y seguridad, Sistema, configuración avanzada del sistema 
(parte superior izquierda), variables de entorno, dentro de variables del sistema , Edite "Path"
y añada la ruta en la que se instalo Mongodb y pulse aceptar dos veces.
 
Para instalar Compass descargelo en aqui https://www.mongodb.com/try/download/compass, extraigalo
y siga las indicaciones.

    Funcionamiento de Mongodb: Es una base de datos NoSQL, enfocado al openSource.
*/

